package Theme5.Task5;

public class Dog {

    public void bark(int i) {
        System.out.println("Перший пес гавкнув " + i + " разів.");
    }

    public void bark(double d) {
        System.out.println("Другий пес гавнкнув на " + d + " разів більше");
    }

    public void bark(char ch) {
        System.out.println("Третій пес не гавкнув, а сказав " + ch);
    }

}